public class Resours {
    private String name;
    private int imageid;
    public Resours(String name,int imageid){
        this.name = name;
        this.imageid = imageid;
    }



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImageid() {
        return imageid;
    }

    public void setImageid(int imageid) {
        imageid = imageid;
    }

}
